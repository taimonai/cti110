import turtle

# Create a turtle object
t = turtle.Turtle()

# Set the color and pen size
t.pencolor("pink")
t.pensize(3)

# Draw the T
t.left(90)
t.forward(100)
t.right(90)
t.forward(25)
t.right(180)
t.forward(50)

# Move to the right
t.penup()
t.goto(100, 0)
t.pendown()
t.penup
t.right
t.pendown

# Draw the F
t.left(90)
t.forward(100)
t.right(90)
t.forward(50)
t.right(180)
t.forward(50)
t.left(90)
t.forward(50)
t.left(90)
t.forward(50)

# Keep the window open
turtle.mainloop()
